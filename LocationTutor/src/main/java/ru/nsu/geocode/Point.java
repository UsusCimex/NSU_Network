package ru.nsu.geocode;

public class Point {
    private double lat;
    private double lng;

    // Геттеры и сеттеры

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLng() {
        return lng;
    }

    public void setLng(double lng) {
        this.lng = lng;
    }
}

